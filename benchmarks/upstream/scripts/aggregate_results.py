"""Aggregate per-(dataset, framework, seed) result JSONs into a tidy summary.

Usage:
    python -m benchmarks.upstream.scripts.aggregate_results \\
        --results-dir benchmarks/upstream/results \\
        --out-csv docs/benchmarks/results_summary.csv \\
        --out-md  docs/benchmarks/results_table.md

Reads every <dataset>_<framework>_<seed>.json under --results-dir, groups by
(dataset, framework), computes mean ± std for metric/wall-clock/RSS, and emits
both a machine-readable CSV and a markdown-table fragment that can be embedded
in docs/benchmarks/v0.5.x-pareto.md.
"""
from __future__ import annotations

import argparse
import json
import statistics
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any, Dict, List, Tuple


def load_all(results_dir: Path) -> List[Dict[str, Any]]:
    out = []
    for p in sorted(results_dir.glob("*.json")):
        try:
            out.append(json.loads(p.read_text()))
        except Exception as exc:
            print(f"  skipping {p.name}: {exc}", file=sys.stderr)
    return out


def aggregate(records: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    groups: Dict[Tuple[str, str, str], List[Dict[str, Any]]] = defaultdict(list)
    for r in records:
        key = (r["dataset"], r["framework"], r.get("metric_name", ""))
        groups[key].append(r)

    rows = []
    for (dataset, framework, metric), recs in sorted(groups.items()):
        metric_vals = [r["metric_value"] for r in recs]
        train_secs = [r["train_seconds"] for r in recs]
        pred_secs = [r["predict_seconds"] for r in recs]
        rss = [r["peak_rss_bytes"] for r in recs]
        rows.append({
            "dataset": dataset,
            "framework": framework,
            "metric": metric,
            "n_seeds": len(recs),
            "metric_mean": statistics.mean(metric_vals),
            "metric_std": statistics.stdev(metric_vals) if len(recs) > 1 else 0.0,
            "train_s_mean": statistics.mean(train_secs),
            "train_s_std": statistics.stdev(train_secs) if len(recs) > 1 else 0.0,
            "predict_s_mean": statistics.mean(pred_secs),
            "predict_s_std": statistics.stdev(pred_secs) if len(recs) > 1 else 0.0,
            "rss_mb_mean": statistics.mean(rss) / 1e6,
            "n_train": recs[0].get("n_train"),
            "n_test": recs[0].get("n_test"),
            "n_features": recs[0].get("n_features"),
            "framework_version": recs[0].get("framework_version", ""),
            "hardware": recs[0].get("hardware", ""),
        })
    return rows


def write_csv(rows: List[Dict[str, Any]], path: Path) -> None:
    if not rows:
        path.write_text("")
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    cols = list(rows[0].keys())
    with open(path, "w") as f:
        f.write(",".join(cols) + "\n")
        for r in rows:
            f.write(",".join(
                f"{r[c]:.6g}" if isinstance(r[c], float) else str(r[c])
                for c in cols
            ) + "\n")


def write_markdown(rows: List[Dict[str, Any]], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)

    by_dataset: Dict[str, List[Dict[str, Any]]] = defaultdict(list)
    for r in rows:
        by_dataset[r["dataset"]].append(r)

    out = ["<!-- Auto-generated by benchmarks.upstream.scripts.aggregate_results -->\n"]
    for dataset, drows in sorted(by_dataset.items()):
        out.append(f"### {dataset}\n")
        n_train = drows[0]["n_train"]
        n_test = drows[0]["n_test"]
        n_features = drows[0]["n_features"]
        out.append(
            f"_n_train={n_train:,}, n_test={n_test:,}, n_features={n_features}, "
            f"hardware: {drows[0]['hardware']}_\n"
        )
        out.append("| Framework | Version | Metric (mean ± std) | Train (s) | Predict (s) | Peak RSS (MB) |")
        out.append("|---|---|---|---|---|---|")
        for r in sorted(drows, key=lambda x: x["framework"]):
            metric_str = f"{r['metric_mean']:.4f} ± {r['metric_std']:.4f}"
            train_str = f"{r['train_s_mean']:.2f} ± {r['train_s_std']:.2f}"
            pred_str = f"{r['predict_s_mean']:.3f} ± {r['predict_s_std']:.3f}"
            rss_str = f"{r['rss_mb_mean']:.0f}"
            out.append(
                f"| {r['framework']} | {r['framework_version']} | "
                f"{metric_str} ({r['metric']}) | {train_str} | {pred_str} | {rss_str} |"
            )
        out.append("")

    path.write_text("\n".join(out))


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__.split("\n")[0])
    ap.add_argument("--results-dir", default=str(Path(__file__).resolve().parents[1] / "results"))
    ap.add_argument("--out-csv", default=None)
    ap.add_argument("--out-md", default=None)
    args = ap.parse_args()

    results_dir = Path(args.results_dir)
    records = load_all(results_dir)
    rows = aggregate(records)

    print(f"Aggregated {len(records)} runs across {len(rows)} (dataset, framework) groups.")
    for r in rows:
        print(
            f"  {r['dataset']:10s}  {r['framework']:25s}  "
            f"{r['metric']}={r['metric_mean']:.4f}±{r['metric_std']:.4f}  "
            f"train={r['train_s_mean']:.2f}s  rss={r['rss_mb_mean']:.0f}MB  ({r['n_seeds']} seeds)"
        )

    if args.out_csv:
        write_csv(rows, Path(args.out_csv))
        print(f"  CSV written: {args.out_csv}")
    if args.out_md:
        write_markdown(rows, Path(args.out_md))
        print(f"  Markdown written: {args.out_md}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
